    private void botonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEnviarActionPerformed
       try
        {
            for(int i=0;i<tablaDatos.getRowCount();i++)
            {
               
                    PreparedStatement pst=cn.prepareStatement("INSERT INTO farmacia(Nombreproducto,Tipoproducto,Cantidadproducto,Nombreproveedor,Sucursal) VALUES(?,?,?,?,?)");
                    pst.setString(1, tablaDatos.getValueAt(i, 0).toString());
                    pst.setString(2, tablaDatos.getValueAt(i, 1).toString());
                    pst.setString(3, tablaDatos.getValueAt(i, 2).toString());
                    pst.setString(4, tablaDatos.getValueAt(i, 3).toString());
                    pst.setString(5, tablaDatos.getValueAt(i, 4).toString());
                    pst.executeUpdate();  
            }
             }catch(Exception e){
                    System.out.print(e.getMessage());
                    }
       JOptionPane.showMessageDialog(this,"ENVIANDO EL PRODUCTO......");
       Tabladebasededatos mostrar=new Tabladebasededatos();
            mostrar.setVisible(true);
    }//GEN-LAST:event_botonEnviarActionPerformed

    private void botonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCancelarActionPerformed
        int respuesta= JOptionPane.showConfirmDialog(this, "Â¿Esta seguro de cancelar el pedido?, los datos se borraran");
        if(respuesta==JOptionPane.YES_OPTION){
            try{
        int fila=tablaDatos.getSelectedRow();
        if(fila>=0){
          modelo.removeRow(fila);
          Ventanaprincipal.contenedor.remove(fila);
             limpiarTabla();
              JOptionPane.showMessageDialog(null, "El pedido que registro se elimino de manera correcta");
       Ventanaprincipal regresar=new Ventanaprincipal();
        regresar.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona una fila a eliminar de la tabla para cancelar el producto de manera correcta y evitar que se acumulen pedidos");
             Ventanaprincipal.contenedor.remove(fila);
             limpiarTabla();
             this.dispose();
              Ventanaprincipal regresar=new Ventanaprincipal();
        regresar.setVisible(true);
     
            this.dispose();
        }}catch(Exception e){
            System.out.println("error en el sistema atrapado");
        }
      }   
    }//GEN-LAST:event_botonCancelarActionPerformed
void limpiarTabla(){
     int elitotal=tablaDatos.getRowCount();
        for(int i=elitotal-1;i>=0;i--){
            modelo.removeRow(i);
        }
        JOptionPane.showMessageDialog(this, "Operacion exitosa");
}
